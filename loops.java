public class loops {
    public static void main(String[] args) {
        for(int i = 0; i <= 10; i++){
            System.out.print(i + " ");
        }
        int i = 0;
        while(i < 11){
            System.out.print(i + " ");
            i++;
            
        }
        int j = 0;

        do{
            System.out.print(j + " ");
            j++;
        }while(j < 11);

    }
}
